package com.prateek.mybatis.spring;

public class BlifError {
    private String code;
    private String reason;
    private String action;
    private String type;

    public String getCode() {
        return code;
    }

    public String getReason() {
        return reason;
    }

    public String getAction() {
        return action;
    }

    public String getType() {
        return type;
    }
}
